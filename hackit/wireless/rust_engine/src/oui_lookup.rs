/// Look up the vendor name for a MAC address.
/// Accepts MAC strings with ":", "-", or no separators (e.g.
/// "B4:B0:24:82:F7:3C", "B4-B0-24-82-F7-3C", or "B4B02482F73C").
/// Returns the vendor name if the OUI is known, otherwise "Unknown".
pub fn oui_lookup(mac: &str) -> &'static str {
    let clean: String = mac
        .chars()
        .filter(|c| c.is_alphanumeric())
        .collect::<String>()
        .to_uppercase();
    if clean.len() < 6 {
        return "Unknown";
    }
    let prefix = format!("{}:{}:{}", &clean[0..2], &clean[2..4], &clean[4..6]);
    oui_map().get(prefix.as_str()).copied().unwrap_or("Unknown")
}

