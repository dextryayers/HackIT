def _normalize(mac: str) -> str:
    return mac.strip().upper().replace("-", ":").replace(" ", "").replace("_", ":")


def _oui_prefix(mac: str) -> str:
    m = _normalize(mac)
    parts = m.split(":")
    if len(parts) >= 3:
        return ":".join(parts[:3])
    if len(m) >= 8:
        return m[:8]
    return m


def _load_system_oui() -> dict[str, str]:
    db: dict[str, str] = {}
    for path in OUI_SOURCES:
        p = Path(path)
        if p.exists():
            try:
                if path.endswith(".csv"):
                    with p.open(newline="", encoding="utf-8", errors="replace") as f:
                        reader = csv.reader(f)
                        for row in reader:
                            if len(row) >= 3 and row[0] == "MA-L":
                                oui = row[1].strip().upper()
                                vendor = row[2].strip()
                                if len(oui) == 6:
                                    key = f"{oui[:2]}:{oui[2:4]}:{oui[4:6]}"
                                    db[key] = vendor
                elif path.endswith(".txt"):
                    text = p.read_text(encoding="utf-8", errors="replace")
                    for m in re.finditer(
                        r"^([0-9A-F]{2})-([0-9A-F]{2})-([0-9A-F]{2})\s+\(hex\)\s+(.+)$",
                        text,
                        re.MULTILINE | re.IGNORECASE,
                    ):
                        key = f"{m.group(1)}:{m.group(2)}:{m.group(3)}".upper()
                        db[key] = m.group(4).strip()
                if db:
                    break
            except Exception:
                continue
    return db


def _build_vendor_map(system_db: dict[str, str]) -> dict[str, str]:
    merged: dict[str, str] = {}
    for prefix, vendor in _FALLBACK_OUI.items():
        merged[prefix] = vendor
    for prefix, vendor in system_db.items():
        v_upper = vendor.upper()
        for known, aliases in ROUTER_VENDORS.items():
            if any(a.upper() in v_upper for a in aliases):
                merged[prefix] = known
                break
        else:
            merged[prefix] = vendor
    return merged


def lookup(mac: str, prefer_unknown: bool = False) -> str:
    global _OUI_LOADED, _OUI_DB
    if not _OUI_LOADED:
        try:
            _OUI_DB = _build_vendor_map(_load_system_oui())
        except Exception:
            _OUI_DB = dict(_FALLBACK_OUI)
        _OUI_LOADED = True
    prefix = _oui_prefix(mac)
    if prefix in _OUI_CACHE:
        return _OUI_CACHE[prefix]
    vendor = _OUI_DB.get(prefix, "Unknown")
    _OUI_CACHE[prefix] = vendor
    return vendor


_OUI_DB: dict[str, str] = {}

def _oui_lookup(mac: str) -> str:
    return lookup(mac)

def vendor_from_airodump(bssid: str, airodump_aps: list[dict]) -> Optional[str]:
    b = bssid.strip().upper()
    for ap in airodump_aps:
        ap_b = ap.get("bssid", "").strip().upper()
        if ap_b == b:
            return ap.get("vendor")
    return None
