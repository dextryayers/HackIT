    def detect_waf(self):
        """Detect WAF with 20+ signatures using probes"""
        print(f"[*] Detecting WAF...")
        waf_payloads = [
            ("?id=' OR 1=1--", "' OR 1=1--"),
            ("?id=<script>alert(1)</script>", "<script>alert(1)</script>"),
            ("?id=../../../etc/passwd", "../../../etc/passwd"),
            ("?id=UNION SELECT NULL--", "UNION SELECT NULL--"),
            ("/../../../etc/passwd", "../../../etc/passwd"),
            ("/CONFIG", "CONFIG"),
            ("/.env", ".env"),
        ]

        for path_suffix, payload in waf_payloads:
            try:
                test_url = f"{self.target_url}{path_suffix}"
                resp = self.session.get(test_url, timeout=self.timeout)

                if resp.status_code in (403, 406, 429, 501, 503):
                    headers = {k.lower(): v for k, v in resp.headers.items()}
                    body = resp.text.lower()
                    cookies = {k: v for k, v in resp.cookies.items()}

                    for waf, sigs in WAF_SIGNATURES.items():
                        for header, pattern in sigs.get("headers", {}).items():
                            if header in headers and re.search(pattern, headers[header], re.I):
                                self.waf_detected = waf
                                print(f"[+] WAF Detected: {waf} (via header)")
                                return waf
                        if sigs.get("body"):
                            for pattern in (sigs["body"] if isinstance(sigs["body"], list) else [sigs["body"]]):
                                if re.search(pattern, body, re.I):
                                    self.waf_detected = waf
                                    print(f"[+] WAF Detected: {waf} (via body)")
                                    return waf

                    # Generic WAF by status
                    if not self.waf_detected:
                        self.waf_detected = f"Generic WAF (Blocked: {resp.status_code})"
                        print(f"[+] WAF Detected: {self.waf_detected}")
                        return self.waf_detected

            except Exception:
                continue

        self.waf_detected = "None"
        print("[+] No WAF detected")
        return "None"

    def extract_js_endpoints(self):
        """Extract endpoints from JS files - smart spidering"""
        print(f"[*] Extracting JS endpoints...")
        try:
            resp = self.session.get(self.target_url, timeout=self.timeout)
            soup = BeautifulSoup(resp.text, "html.parser")

            # Find all script tags
            js_urls = set()
            for script in soup.find_all("script", src=True):
                js_url = urljoin(self.target_url, script["src"])
                js_urls.add(js_url)
                self.all_js_urls.append(js_url)

            # Find inline JS
            inline_scripts = []
            for script in soup.find_all("script"):
                if script.string:
                    inline_scripts.append(script.string)

            # Also try common JS file names
            for js_file in COMMON_JS_FILES:
                js_url = urljoin(self.target_url, f"/{js_file}")
                js_urls.add(js_url)

            # Fetch and parse all JS files concurrently
            def fetch_js(js_url):
                try:
                    r = self.session.get(js_url, timeout=self.timeout)
                    if r.status_code == 200:
                        return js_url, r.text
                except Exception:
                    pass
                return js_url, None

            with ThreadPoolExecutor(max_workers=self.threads) as executor:
                futures = {executor.submit(fetch_js, url): url for url in js_urls}
                for future in as_completed(futures):
                    js_url, js_content = future.result()
                    if js_content:
                        self._parse_js_content(js_content, js_url)

            # Also parse inline scripts
            for inline in inline_scripts:
                self._parse_js_content(inline)

            if self.all_js_urls:
                print(f"[+] Found {len(self.all_js_urls)} JS files")
            print(f"[+] Extracted {len(self.found_endpoints)} potential endpoints from JS")

        except Exception as e:
            print(f"[!] JS extraction failed: {e}")

    def _parse_js_content(self, content, source_url=""):
        """Parse JS content for API endpoints, routes, and patterns"""
        patterns = [
            r'["\'](/[a-zA-Z0-9_\-./]+)["\']',     # "/api/v1/users"
            r'["\'](/[a-zA-Z0-9_\-./]+\.[a-z]{2,4})["\']',  # "/path/file.js"
            r'url\s*[:=]\s*["\'](/[^"\']+)["\']',   # url: "/api/users"
            r'path\s*[:=]\s*["\'](/[^"\']+)["\']',  # path: "/users/:id"
            r'endpoint\s*[:=]\s*["\'](/[^"\']+)["\']',  # endpoint: '/api'
            r'route\s*[:=]\s*["\'](/[^"\']+)["\']',  # route: '/home'
            r'api\s*[:=]\s*["\'](/[^"\']+)["\']',   # api: '/v2/users'
            r'fetch\(["\'](/[^"\']+)["\']',           # fetch('/api/users')
            r'axios\.\(["\'](/[^"\']+)["\']',          # axios('/api/users')
            r'axios\.get\(["\'](/[^"\']+)["\']',       # axios.get('/api/users')
            r'axios\.post\(["\'](/[^"\']+)["\']',      # axios.post('/api/users')
            r'\$\.ajax\(["\'](/[^"\']+)["\']',         # $.ajax('/api')
            r'\$\.get\(["\'](/[^"\']+)["\']',           # $.get('/api')
            r'\$\.post\(["\'](/[^"\']+)["\']',          # $.post('/api')
            r'XMLHttpRequest\.open\(["\'](?:GET|POST|PUT|DELETE)["\'],\s*["\'](/[^"\']+)["\']',  # XHR
            r'router\.(?:get|post|put|delete|patch|all)\(["\'](/[^"\']+)["\']',  # Express router
            r'apiVersion\s*=\s*["\']([^"\']+)["\']',  # API version strings
            r'controller\s*[:=]\s*["\']([^"\']+)["\']',  # controller names
            r'action\s*[:=]\s*["\']([^"\']+)["\']',   # action names
            r'templateUrl\s*[:=]\s*["\'](/[^"\']+)["\']',  # Angular templates
            r'component\s*[:=]\s*["\']([^"\']+)["\']', # component names
            r'resolver\s*[:=]\s*["\']([^"\']+)["\']',  # resolver paths
        ]

        for pattern in patterns:
            matches = re.findall(pattern, content)
            for m in matches:
                m = m.strip().split("?")[0].split("#")[0]
                if len(m) > 2 and not m.startswith("//"):
                    clean = m.lstrip("/")
                    if clean and clean not in self.found_endpoints:
                        # Filter out common false positives
                        if not any(fp in clean.lower() for fp in
                                   ["http://", "https://", "www.", ".com", ".org", ".net", "javascript:"]):
                            self.found_endpoints.add(clean)

    def find_backups(self):
        """Find backup/config files"""
        print(f"[*] Scanning for backup & config files...")
        domain = urlparse(self.target_url).netloc.replace("www.", "").split(".")[0]

        candidates = []
        for bf in BACKUP_FILES:
            for ext in BACKUP_EXTENSIONS:
                candidates.append(f"/{bf}{ext}")
                candidates.append(f"/{domain}{ext}")
                candidates.append(f"/backup/{bf}{ext}")
                candidates.append(f"/{bf}/{bf}{ext}")

        # Add common config files
        candidates.extend(COMMON_ENDPOINTS)

        def check_path(path):
            try:
                url = f"{self.target_url}{path}"
                resp = self.session.head(url, timeout=3)
                if resp.status_code in (200, 401, 403):
                    self.found_endpoints.add(path.lstrip("/"))
                    return path
                # Some servers don't support HEAD
                if resp.status_code == 405:
                    resp2 = self.session.get(url, timeout=3, stream=True)
                    if resp2.status_code in (200, 401, 403):
                        self.found_endpoints.add(path.lstrip("/"))
                        return path
            except Exception:
                pass
            return None

        checked = 0
        with ThreadPoolExecutor(max_workers=self.threads) as executor:
            futures = {executor.submit(check_path, p): p for p in candidates[:100]}
            for future in as_completed(futures):
                result = future.result()
                checked += 1
                if result:
                    print(f"  [+] Found: {result}")
                if checked % 25 == 0:
                    print(f"  [*] Checked {checked}/{min(len(candidates), 100)}...")

        if len(candidates) > 100:
            print(f"  [*] Plus {len(candidates) - 100} additional paths queued")

