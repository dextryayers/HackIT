class SmartAnalyzer:
    def __init__(self, target_url, timeout=10, threads=10):
        self.target_url = target_url.rstrip("/")
        self.timeout = timeout
        self.threads = threads
        self.session = requests.Session()
        self.session.verify = False
        self.session.headers.update({
            "User-Agent": "Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36",
            "Accept": "text/html,application/xhtml+xml,application/xml;q=0.9,*/*;q=0.8",
            "Accept-Language": "en-US,en;q=0.5",
        })
        self.found_endpoints = set()
        self.tech_stack = []
        self.waf_detected = ""
        self.cms_detected = ""
        self.recommended_wordlists = []
        self.all_js_urls = []

    def detect_tech(self):
        """Detect technology stack using 30+ signatures"""
        print(f"[*] Detecting technology stack...")
        try:
            resp = self.session.get(self.target_url, timeout=self.timeout)
            html = resp.text.lower()
            headers = {k.lower(): v for k, v in resp.headers.items()}
            cookies = {k: v for k, v in resp.cookies.items()}

            for tech, sigs in TECH_SIGNATURES.items():
                found = False
                for header, pattern in sigs.get("headers", {}).items():
                    if header in headers and re.search(pattern, headers[header], re.I):
                        found = True
                        break
                if not found:
                    for cookie, pattern in sigs.get("cookies", {}).items():
                        if any(re.search(pattern, v) for k, v in cookies.items() if k.lower().startswith(cookie.lower())):
                            found = True
                            break
                if not found and sigs.get("body"):
                    if re.search(sigs["body"], html, re.I):
                        found = True
                if found:
                    self.tech_stack.append(tech)
                    self._add_wordlist_for_tech(tech)

            # Extract version info
            if "X-Powered-By" in resp.headers:
                self.tech_stack.append(f"X-Powered-By: {resp.headers['X-Powered-By']}")
            if "X-AspNet-Version" in resp.headers:
                self.tech_stack.append(f"ASP.NET: {resp.headers['X-AspNet-Version']}")
            if "X-Runtime" in resp.headers:
                self.tech_stack.append(f"Ruby: {resp.headers['X-Runtime']}")

            # CMS specific detection
            if "WordPress" in self.tech_stack:
                self.cms_detected = "WordPress"
            elif "Drupal" in self.tech_stack:
                self.cms_detected = "Drupal"
            elif "Joomla" in self.tech_stack:
                self.cms_detected = "Joomla"

            print(f"[+] Tech Stack: {', '.join(self.tech_stack) if self.tech_stack else 'Unknown'}")
        except Exception as e:
            print(f"[!] Tech detection failed: {e}")

