    def run_smart_analysis(self):
        """Run full analysis pipeline"""
        print(f"\n{'='*60}")
        print(f"  Smart Analysis: {self.target_url}")
        print(f"{'='*60}\n")

        waf = self.detect_waf()
        self.detect_tech()
        self.generate_smart_wordlist()
        self.extract_js_endpoints()
        # self.find_backups()  # Optionally enable for deeper analysis

        # Deduplicate
        self.found_endpoints = set(
            ep for ep in self.found_endpoints
            if ep and len(ep) > 1 and not ep.startswith("http")
        )

        return {
            "waf": self.waf_detected,
            "cms": self.cms_detected,
            "tech": self.tech_stack,
            "endpoints": sorted(self.found_endpoints),
            "recommended_wordlists": self.recommended_wordlists,
            "js_files": self.all_js_urls,
        }

    def save_analysis(self, output_path="smart_analysis.json"):
        """Save analysis to JSON for Go engine consumption"""
        results = self.run_smart_analysis()
        with open(output_path, "w") as f:
            json.dump(results, f, indent=2)
        print(f"\n[+] Smart analysis saved to: {output_path}")
        print(f"[+] Endpoints discovered: {len(results['endpoints'])}")
        print(f"[+] Tech stack: {len(results['tech'])} items")
        print(f"[+] Recommended categories: {results['recommended_wordlists']}")
        return results


