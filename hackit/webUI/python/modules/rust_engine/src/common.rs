#[derive(Serialize, Deserialize, Clone)]
pub struct ProgressEvent {
    pub event: String,
    pub module: String,
    pub status: String,
    pub ts: u64,
    pub total: Option<u64>,
    pub progress: Option<u64>,
}

impl ProgressEvent {
    pub fn new(module: &str, status: &str) -> Self {
        Self { event: "progress".into(), module: module.into(), status: status.into(), ts: now_ms(), total: None, progress: None }
    }
    pub fn with_progress(module: &str, status: &str, progress: u64, total: u64) -> Self {
        Self { event: "progress".into(), module: module.into(), status: status.into(), ts: now_ms(), total: Some(total), progress: Some(progress) }
    }
}

#[macro_export]
macro_rules! progress {
    ($module:expr, $status:expr) => {
        println!("{}", serde_json::to_string(&crate::common::ProgressEvent::new($module, $status)).unwrap());
    };
}

#[macro_export]
macro_rules! progress_done {
    ($module:expr) => {
        println!("{}", serde_json::to_string(&crate::common::ProgressEvent::new($module, "done")).unwrap());
    };
}

fn now_ms() -> u64 { std::time::SystemTime::now().duration_since(std::time::UNIX_EPOCH).unwrap_or_default().as_millis() as u64 }

pub struct ScopedTimer {
    pub label: String,
    pub start: Instant,
}

impl ScopedTimer {
    pub fn new(label: &str) -> Self { Self { label: label.into(), start: Instant::now() } }
}

impl Drop for ScopedTimer {
    fn drop(&mut self) {
        let elapsed = self.start.elapsed().as_millis();
        eprintln!("[timing] {}: {}ms", self.label, elapsed);
    }
}

#[macro_export]
macro_rules! timed {
    ($label:expr) => { let _timer = crate::common::ScopedTimer::new($label); };
}

